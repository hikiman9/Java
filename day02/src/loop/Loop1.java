package loop;

public class Loop1 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("wffwe");
        }
        int sum2 = 0;
        int j = 1;
        while(j <= 10){
            sum2 += j;
            j += 1;
            System.out.println(sum2);
        }
    }
}
